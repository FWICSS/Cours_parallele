public class Lettre {
    private char lettre;
    private int nb;
    public Lettre(char c, int n) {
        lettre = c ;
        nb = n ;
        }
    public char getLettre(){
        return lettre ;
        }
    public int getNb(){
        return nb ;
        }
public void setNb(int n){
        nb = n ;
        }
}
